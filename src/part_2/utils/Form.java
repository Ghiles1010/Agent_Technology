package part_2.utils;


public class Form {

    private String from;
    private String to;
    private String date_depart;
    private String date_return;
    private String number_persons;
    private String number_kids;
    private String number_elders;



    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getDate_depart() {
        return date_depart;
    }

    public void setDate_depart(String date_depart) {
        this.date_depart = date_depart;
    }

    public String getDate_return() {
        return date_return;
    }

    public void setDate_return(String date_return) {
        this.date_return = date_return;
    }

    public String getNumber_persons() {
        return number_persons;
    }

    public void setNumber_persons(String number_persons) {
        this.number_persons = number_persons;
    }

    public String getNumber_kids() {
        return number_kids;
    }

    public void setNumber_kids(String number_kids) {
        this.number_kids = number_kids;
    }

    public String getNumber_elders() {
        return number_elders;
    }

    public void setNumber_elders(String number_elders) {
        this.number_elders = number_elders;
    }
}
